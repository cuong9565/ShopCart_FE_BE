	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	CONSTRAINT "cart_item_user_product_unique" UNIQUE("user_id","product_id"),
	CONSTRAINT "uk2dhdhp9i9ld88nv1bejg9hbx7" UNIQUE("user_id","product_id"),
	CONSTRAINT "cart_item_quantity_check" CHECK ((quantity > 0))
);
CREATE TABLE "category" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"name" varchar(255) NOT NULL CONSTRAINT "category_name_key" UNIQUE,
	"created_at" timestamp DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE "coupons" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"code" varchar(50) NOT NULL CONSTRAINT "coupons_code_unique" UNIQUE,
	"discount_value" numeric(19, 2) NOT NULL,
	"expiry_date" timestamp,
	"min_order_value" numeric(19, 2) DEFAULT '0' NOT NULL,
	"max_discount" numeric(19, 2),
	"discount_type" varchar(20) NOT NULL,
	"start_date" timestamp,
	"usage_per_user" integer DEFAULT 1 NOT NULL,
	"status" varchar(20) DEFAULT 'ACTIVE' NOT NULL,
	"created_at" timestamp DEFAULT CURRENT_TIMESTAMP,
	"coupon_scope" varchar(255) NOT NULL,
	CONSTRAINT "coupons_discount_type_check" CHECK (((discount_type)::text = ANY ((ARRAY['FIXED'::character varying, 'PERCENT'::character varying])::text[]))),
	CONSTRAINT "coupons_scope_check" CHECK (((coupon_scope)::text = ANY ((ARRAY['ORDER'::character varying, 'SHIPPING'::character varying])::text[]))),
	CONSTRAINT "coupons_status_check" CHECK (((status)::text = ANY ((ARRAY['ACTIVE'::character varying, 'INACTIVE'::character varying])::text[])))
);
CREATE TABLE "inventory" (
	"product_id" uuid PRIMARY KEY,
	"quantity" integer NOT NULL
);
CREATE TABLE "order_coupons" (
	"order_coupon_id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
