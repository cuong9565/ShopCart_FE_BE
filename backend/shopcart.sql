	"coupon_id" uuid NOT NULL
);
CREATE TABLE "order_items" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"order_id" uuid NOT NULL,
	"product_id" uuid NOT NULL,
	"quantity" integer NOT NULL,
	"price" numeric(19, 2) NOT NULL
);
CREATE TABLE "order_payments" (
	"id" uuid DEFAULT uuid_generate_v4(),
	"order_id" uuid NOT NULL CONSTRAINT "payments_order_id_unique" UNIQUE,
	"method" integer NOT NULL,
	"status" varchar(50) DEFAULT 'PENDING',
	"paid_at" timestamp,
	"payment_method_id" uuid,
	CONSTRAINT "payments_pkey" PRIMARY KEY("id")
);
CREATE TABLE "orders" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"user_id" uuid NOT NULL,
	"address_id" uuid NOT NULL,
	"subtotal" numeric(19, 2) NOT NULL,
	"discount" numeric(19, 2),
	"shipping_fee" numeric(19, 2),
	"final_price" numeric(19, 2) NOT NULL,
	"status" varchar(50) DEFAULT 'PENDING',
	"created_at" timestamp DEFAULT CURRENT_TIMESTAMP,
	"shipping_method_id" uuid
);
CREATE TABLE "payment_methods" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"code" varchar(50) NOT NULL CONSTRAINT "payment_methods_code_key" UNIQUE,
	"name" varchar(255) NOT NULL,
	"is_active" boolean DEFAULT true,
	"created_at" timestamp DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE "product" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"name" varchar(255) NOT NULL,
	"price" numeric(12, 2) NOT NULL,
	"description" text,
	"status" varchar(255) DEFAULT 'ACTIVE',
	"created_at" timestamp DEFAULT CURRENT_TIMESTAMP,
	"category_id" uuid,
	"slug" varchar(255) CONSTRAINT "product_slug_key" UNIQUE,
	"is_featured" boolean DEFAULT false
);
CREATE TABLE "product_image" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
	"product_id" uuid NOT NULL,
	"image_url" text NOT NULL,
	"sort_order" integer DEFAULT 0,
	"created_at" timestamp DEFAULT CURRENT_TIMESTAMP,
	"is_thumbnail" boolean DEFAULT false
);
CREATE TABLE "shipping_methods" (
	"id" uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
